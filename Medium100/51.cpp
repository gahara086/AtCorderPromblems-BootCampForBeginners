int main() {
    // int N;
    int N;
    cin >> N;

    int A[N];
    int temp_A[N];
    for (int i = 0; i < N; i++) {
        cin >> A[i];
        temp_A[i] = A[i];
    }

    sort(A, A + N);

    map<int, int> idx;
    for (int i = 0; i < N; i++) {
        idx[A[i]] = i;
    }

    for (int i = 0; i < N; i++) {
        int j = idx[temp_A[i]];
        int ans = 0;
        if (j < N / 2) {
            ans = A[N / 2];
        } else {
            ans = A[N / 2 - 1];
        }
        cout << ans << endl;
    }

    return 0;
}
